package com.DS1.Strings;

public class PrettyPrinting {
    public static void main(String[] args) {
        float a = 234.45352f;
        System.out.printf("Corrected Number is %.3f", a);
        System.out.println();

        System.out.println(Math.PI);

        System.out.printf("Pie : %.2f", Math.PI);
        System.out.println();

        System.out.printf("Hello My name is %s and I'm doing %s", "Samyak", "Java");
    }
}
